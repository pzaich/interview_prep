var Cell = function (value) {
  this.value = value
  this.touched = false

  this.setAdjacents = function (adjacents) {
    this.adjacents = adjacents
  }

  this.touchAdjacents = function () {
    this.adjacents.forEach(function (cell) {
      cell.touch()
    })
  }

  this.nearbyMines = function () {
    return this.adjacents.filter((cell) => (cell.value === "M"))
  }

  this.touch = function () {
    var nearbyMines = this.nearbyMines().length;

    if (this.touched) {
      return
    } else if (this.value === "E" && nearbyMines === 0) {
      this.value = "B"
      this.touched = true
      this.touchAdjacents()
    } else if (this.value === "E" && nearbyMines > 0) {
      this.value = nearbyMines.toString()
      this.touched = true
    } else {
      // do nothing
    }
  }

  this.click = function () {
    if (this.value === 'M') {
      this.touched = true
      this.value = "X"
      console.log(this.value)
      return
    } else {
      var minesNearby = this.nearbyMines()

      if (minesNearby.length > 0) {
        this.touched = true
        this.value = minesNearby.length.toString()
      } else {
        this.value = "B"
        this.touched = true
        this.touchAdjacents()
      }
    }
  }
}

function getCell(board, x,y) {
  var row = board[y]

  if (row) {
    return row[x]
  }
}

var updateBoard = function(board, clickPoints) {
  // check for entirely empty board
  var isAllBlank = true

  var boardWithCells = []

  // initialize with adjacents
  board.forEach(function (row, y) {
    var newRow = []
    row.forEach(function (cellValue, x) {
      var cell = new Cell(cellValue)

      if (cellValue !== "E") {
        isAllBlank = false
      }
    newRow.push(cell)
    })
    boardWithCells.push(newRow)
  })

  if (isAllBlank) {
    return board.map((row) => {
      return row.map((cell) => "B")
    })
  }

  // build adjacents using initialized cells
  boardWithCells.forEach(function (row, y) {
    row.forEach(function (cell, x) {
      var tl = getCell(boardWithCells, x-1, y-1)
      var tm = getCell(boardWithCells, x, y-1)
      var tr = getCell(boardWithCells, x+1, y-1)

      var ml = getCell(boardWithCells, x-1, y)
      var mr = getCell(boardWithCells, x+1, y)

      var bl = getCell(boardWithCells, x-1, y+1)
      var bm = getCell(boardWithCells, x, y+1)
      var br = getCell(boardWithCells, x+1, y+1)

      var adjacents = [tl, tm, tr, ml, mr, bl, bm, br].filter(function (ad) { return ad })

      cell.setAdjacents(adjacents)
    })
  })

  var clickedCell = boardWithCells[clickPoints[0]][clickPoints[1]]
  clickedCell.click()

  // return board back to string
  return board.map(function (row, y) {
    return row.map(function (cell, x) {
      return boardWithCells[y][x].value
    })
  })
};

